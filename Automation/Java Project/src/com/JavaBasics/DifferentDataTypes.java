package com.JavaBasics;

public class DifferentDataTypes {

	public static void main(String[] args) {
	
	//Data type variable = assigned value
	int a = 10;
	a = 30;
	String b = "Hello";
	double c = 10.123;
	char d = 'P';
	String p = "10";
	
	System.out.println("The integer value of a is:" +a);
	System.out.println("The string value of b is:"+b);
	System.out.println("The double value of c is:"+c);
	System.out.println("The character value of d is:"+d);
	System.out.println("The string value of p is:"+p);
	
	
	

	}

}
