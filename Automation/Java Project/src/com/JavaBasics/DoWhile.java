package com.JavaBasics;

public class DoWhile {
	public static void main(String[] args)
	{
		int h=1;
		do {
			System.out.println("Live Tech");
			h=h+1;	
		}
		while(h<=5);
		
		System.out.println("***********************");
		int p=5;
		do {
			System.out.println("Testing");
			p--;
		}
		while(p>=1);
	}

}
